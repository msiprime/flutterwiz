///boiler plate code for the liskov substitution principle
///Liskov Substitution Principle states that objects of a superclass shall be replaceable with objects of its subclasses without affecting the functionality of the program.
///In other words, the objects of a superclass should be replaceable with objects of its subclasses without breaking the application.
///This principle is based on the Open/Closed Principle.
///It ensures that a class that inherits from another class can be used in place of the parent class without any unexpected behavior.
///The Liskov Substitution Principle is important because it helps to ensure that the code is flexible and easy to maintain.
/// It also helps to ensure that the code is easy to understand and that it is easy to test.
